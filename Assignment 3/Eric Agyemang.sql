CREATE OR REPLACE FUNCTION get_full_name
(
state_abbr_param VARCHAR2
)
RETURN VARCHAR2
AS
 state_full_name VARCHAR2(20);
 BEGIN
  SELECT full --join
  INTO state_full_name
  FROM states
  WHERE abbr = state_abbr_param;
  
  RETURN state_full_name;
  END;
  /
