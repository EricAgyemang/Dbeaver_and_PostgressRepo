CREATE OR REPLACE PROCEDURE state_pop_change
(
state_abbr_param VARCHAR2,
new_pop_param   NUMBER
)
AS
BEGIN
  UPDATE states
  SET population = new_pop_param
  WHERE abbr = state_abbr_param;
END;
