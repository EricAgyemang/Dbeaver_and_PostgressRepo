--3. Use the function that when given the 2-character abbreviation of a state will return the number of Sears stores closing in the state.
CREATE OR REPLACE FUNCTION number_closing
(
state_abbr_param VARCHAR2
)
RETURN NUMBER
AS
 store_closings NUMBER;
 BEGIN
  SELECT COUNT(*)
  INTO store_closings
  FROM closings
  WHERE state = state_abbr_param;
  
  RETURN store_closings;
  END;
  /
select number_closing('CO') as state from dual;


